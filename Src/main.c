/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Configure the peripherals common clocks */
  PeriphCommonClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  MX_I2C1_Init();
  MX_RTC_Init();
  MX_SPI2_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM15_Init();
  MX_TIM16_Init();
  MX_USB_PCD_Init();

  // Initialize C++ objects
  initialize_static_objects();
  // main.c is automatically generated, but compiled as C code, which has trouble with the C++ code we're trying to include
  // as such, we've wrapped the while loop here into mainProgram() so we can compile that as C++ while still taking advantage
  // of the automatically generated main.c.

  main_loop();
}

